package com.example.agriculturalproject.Models;

public class Boxes {
    String img , name;

    public Boxes() {
    }

    public Boxes(String img, String name) {
        this.img = img;
        this.name = name;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
